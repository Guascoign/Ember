/**
 * @brief   初始化MPU6050
 *
 * @param   p_MPUDev   MPU6050存储器设备结构体指针
 * 
 * @return  返回值:0,成功   1,错误
 */
static uint8_t mpu6050_Init(struct MPU_Device *p_MPUDev)
{
    #ifdef Debug_Logs
        printf("MPU_Init\r\n");
    #endif   
	uint8_t res;
 	p_MPUDev->IIC_Device->IIC_Init(p_MPUDev->IIC_Device);//IIC初始化
	p_MPUDev->MPU_WriteByte(p_MPUDev,MPU_PWR_MGMT1_REG,0X80);	//复位MPU6050
	HAL_Delay(100);
	p_MPUDev->MPU_WriteByte(p_MPUDev,MPU_PWR_MGMT1_REG,0X00);	//唤醒MPU6050 
//	p_MPUDev->MPU_Set_Gyro_Fsr(3);					//陀螺仪传感器,±2000dps
//	p_MPUDev->MPU_Set_Accel_Fsr(0);					//加速度传感器,±2g
//	p_MPUDev->MPU_Set_Rate(50);						//设置采样率50Hz
	p_MPUDev->MPU_WriteByte(p_MPUDev,MPU_INT_EN_REG,0X00);	//关闭所有中断
	p_MPUDev->MPU_WriteByte(p_MPUDev,MPU_USER_CTRL_REG,0X00);	//I2C主模式关闭
	p_MPUDev->MPU_WriteByte(p_MPUDev,MPU_FIFO_EN_REG,0X00);	//关闭FIFO
	p_MPUDev->MPU_WriteByte(p_MPUDev,MPU_INTBP_CFG_REG,0X80);	//INT引脚低电平有效
	res=p_MPUDev->MPU_ReadByte(p_MPUDev,MPU_DEVICE_ID_REG);
	if(res==MPU_ADDR)//器件ID正确
	{
		p_MPUDev->MPU_WriteByte(p_MPUDev,MPU_PWR_MGMT1_REG,0X01);	//设置CLKSEL,PLL X轴为参考
		p_MPUDev->MPU_WriteByte(p_MPUDev,MPU_PWR_MGMT2_REG,0X00);	//加速度与陀螺仪都工作
//		p_MPUDev->MPU_Set_Rate(50);						//设置采样率为50Hz
 	}else return 1;
	return 0;
}



